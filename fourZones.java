import java.util.ArrayList;

public class fourZones {
    ArrayList<Point> top_left, top_right, bottom_left, bottom_right;

    fourZones(ArrayList<Point> top_left, ArrayList<Point> top_right, ArrayList<Point> bottom_left, ArrayList<Point> bottom_right){
        this.top_left = top_left;
        this.top_right = top_right;
        this.bottom_left = bottom_left;
        this.bottom_right = bottom_right;
    }
}
